package com.review;

import java.io.Serializable;

public class User implements Serializable {
    // 序列化版本号id
    private static final long SERIALIZABLEID=384294923948932l;
    
    private int id;

    private String name;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    @Override
    public String toString() {
        return "User{" +
                "id=" + id +
                ", name='" + name + '\'' +
                '}';
    }
}
